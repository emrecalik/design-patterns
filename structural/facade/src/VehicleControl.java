public class VehicleControl {

    public void stepOnAcceleratorPedal() {
        System.out.println("Accelerator pedal is pushed...");
    }

    public void rollWheels() {
        System.out.println("Wheels are rolling...");
    }

    public void stepOffAcceleratorPedal() {
        System.out.println("Accelerator pedal is released...");
    }

    public void stepOnBrake() {
        System.out.println("Brake system is activated...");
    }

    public void stopVehicle() {
        System.out.println("Wheels are not rolling and car is stopped now...");
    }
}
